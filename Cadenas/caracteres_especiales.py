# Programa: Ejemplo de caracteres especiales en Python

# Salto de línea \n
mensaje1 = "Hola\nMundo"
print(mensaje1)

#Tabulación \t
#Imprimimos una guía numérica para ver las posiciones
print("123412341234123412341234123412341234")
mensaje2 = "\t\tJuan Perez"
print(mensaje2)

#Comillas dentro de cadena
mensaje3 = "Ella dijo: \"Hola a todos\""
print(mensaje3)

#Barra invertida
mensaje4 = "C:\\Usuarios\\Salo\\Documentos"
print(mensaje4)

#Cadenas cruda (raw string) r
mensaje5 = r"C:\Usuarios\Salo\Documentos"
print(mensaje5)