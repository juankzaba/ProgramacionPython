print('*** Función Sumar')

# Definimos la función
def sumar(a,b):
    resultado_suma = a+b
    return resultado_suma

# Llamar a la función
resultado_funcion = sumar(8,5)
print(f"Resultado función sumar:{resultado_funcion}")

resultado_funcion = sumar(9,9)
print(f"Resultado función sumar:{resultado_funcion}")